  // ---- Save search context for downstream pages --------------
  function saveContext(q, brand){
    try {
      sessionStorage.setItem('volvix_last_search', JSON.stringify({
        query: q,
        giro: q,
        business_name: q,
        brand: brand && brand.brand || null,
        brand_url: brand && brand.url || null,
        ts: Date.now(),
      }));
    } catch (_) {}
  }

  // ---- Override quickSearch + searchGiro ---------------------
  function installOverrides(){
    var prevQuick = window.quickSearch;
    window.quickSearch = function(text){
      var brand = resolve(text);
      if (brand && brand.url) {
        saveContext(text, brand);
        window.location.href = brand.url;
        return;
      }
      // HOTFIX V8.4: antes de caer al template plano, intentar fallback a hero más cercano
      var fbUrl = fallbackToClosestHero(text);
      if (fbUrl) {
        saveContext(text, { brand: 'Fallback (closest hero)', url: fbUrl });
        window.location.href = fbUrl;
        return;
      }
      // Fallback final: comportamiento original (sólo si query muy corta)
      if (typeof prevQuick === 'function') return prevQuick(text);
      var inp = document.getElementById('giro-input');
      if (inp) { inp.value = text; if (typeof window.searchGiro === 'function') window.searchGiro(); }
    };

    var prevSearch = window.searchGiro;
    window.searchGiro = function(){
      var inp = document.getElementById('giro-input');
      var q = inp ? (inp.value || '').trim() : '';
      if (q) {
        var brand = resolve(q);
        if (brand && brand.url) {
          saveContext(q, brand);
          window.location.href = brand.url;
          return;
        }
        // HOTFIX V8.4: fallback a hero más cercano antes de template plano
        var fbUrl = fallbackToClosestHero(q);
        if (fbUrl) {
          saveContext(q, { brand: 'Fallback (closest hero)', url: fbUrl });
          window.location.href = fbUrl;
          return;
        }
      }
      if (typeof prevSearch === 'function') return prevSearch();
    };
  }

  // ---- Rewrite landing links cuando se rendericen -----------
  function rewriteLinks(root){
    if (!root || !root.querySelectorAll) return;
    var links = root.querySelectorAll(
      'a[href*="landing_dynamic.html?giro="], a[href*="/landing-"], a[href^="landing-"]'
    );
    links.forEach(function(a){
      if (a.dataset.vlxRouted) return;
      var href = a.getAttribute('href') || '';
      var key = null;
      var m1 = href.match(/[?&]giro=([^&#]+)/);
      var m2 = href.match(/\/?landing-([^?#.]+)/);
      if (m1) key = decodeURIComponent(m1[1]);
      else if (m2) key = decodeURIComponent(m2[1]);
      if (!key) return;
      // Normalize the key (some come with hyphens or accents)
      key = norm(key).replace(/\s+/g, '_');
      var brand = VLX_BRANDS[key] || resolve(key);
      if (brand && brand.url) {
        a.dataset.vlxRouted = '1';
        a.dataset.vlxOriginal = href;
        a.setAttribute('href', brand.url);
        return;
      }
      // HOTFIX V8.4: si no hay match, también redirigir al hero más cercano
      var fbUrl = fallbackToClosestHero(key);
      if (fbUrl) {
        a.dataset.vlxRouted = '1';
        a.dataset.vlxOriginal = href;
        a.dataset.vlxFallback = '1';
        a.setAttribute('href', fbUrl);
      }
    });
  }

  function init(){
    installOverrides();
    rewriteLinks(document);

    // Watch para cuando el JS renderiza giros dinámicamente
    if (window.MutationObserver) {
      var mo = new MutationObserver(function(muts){
        muts.forEach(function(m){
          m.addedNodes.forEach(function(n){
            if (n.nodeType === 1) rewriteLinks(n);
          });
        });
      });
      mo.observe(document.body, { childList: true, subtree: true });
    }
  }

  // V6 FIX: el marketplace.html dentro de su DOMContentLoaded callback hace
  // `window.quickSearch = quickSearch (local)` en la linea 1662 y `window.searchGiro = ...`
  // en la 2138. Si el router instala sus overrides durante DOMContentLoaded
  // tambien, el orden de listeners hace que el marketplace SOBREESCRIBA el
  // override del router. Para evitarlo, instalamos en `window.load` que dispara
  // DESPUES de TODOS los DOMContentLoaded callbacks. Adicionalmente reaplicamos
  // 50ms despues por si algun otro script tardio toca window.quickSearch.
  function safeInit(){
    init();
    setTimeout(function(){
      // Re-aplicar override si algun otro script lo reemplazo despues
      var qs = String(window.quickSearch || '');
      if (qs.indexOf('VLX_BRANDS') === -1 && qs.indexOf('brand') === -1) {
        installOverrides();
      }
    }, 50);
  }
  if (document.readyState === 'complete') {
    safeInit();
  } else {
    window.addEventListener('load', safeInit);
  }
